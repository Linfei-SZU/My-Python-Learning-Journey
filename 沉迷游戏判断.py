game_time = float(input("这是大学生游戏时间健康评估系统，请输入您一周内平均每天的游戏时间(小时): "))

print(f"\n您的日均游戏时间为: {game_time}小时")#让用户输入日均游戏时间
print("评估结果: ", end="")

if game_time < 0:#日均游戏时间不能是负数
    print("请重新输入")
elif game_time >24:#一天只有24小时不能超过
    print("请重新输入")
elif game_time <= 1:#进行时长判断
    print("健康")
    print("建议: 您的游戏时间非常合理，继续保持这种健康的娱乐习惯。")
    print("说明: 适度的游戏可以帮助放松心情，不会影响学习和社交活动。")
elif game_time <= 2:
    print("适度")
    print("建议: 您的游戏时间在合理范围内，但请注意保持学习和娱乐的平衡。")
    print("说明: 每天1-2小时的游戏时间通常不会对学业产生负面影响。")
elif game_time <= 4:
    print("需要注意")
    print("建议: 建议适当减少游戏时间，确保优先完成学习任务。")
    print("说明: 每天2-4小时的游戏时间可能开始影响学习效率和睡眠质量。")
elif game_time > 4:
    print("已沉迷")
    print("建议: 强烈建议大幅减少游戏时间，考虑制定时间管理计划，必要时寻求老师或心理咨询师的帮助。")
    print("说明: 每天4小时以上的游戏时间已经严重影响学习、社交和身心健康。") 
else :
    print("请重新输入数据")#可能输入了文本数据，报错